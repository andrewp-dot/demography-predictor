def print_available_states(dataset_path: str) -> None:
    """
    Print available populations in the dataset.

    Args:
        dataset_path (str): Path to the dataset.
    """
    df = pd.read_csv(dataset_path)
    states = list(df["country_name"].unique())

    pprint.pprint(states)


class StatesGroups(BaseModel):
    def get_states_corresponding_group(self, state: str) -> List[str]:
        for group_states in self.model_dump().values():
            if state in group_states:
                return group_states


